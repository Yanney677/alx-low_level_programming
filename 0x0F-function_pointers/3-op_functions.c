#include "3-calc.h"

/**
 * op_add - function that add two integers
 * @a: first integer
 * @b: second integer
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}



/**
 * op_sub - function that subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: the subtration of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function that multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: the multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function that divides two integers
 * @a: first integer
 * @b: second integer
 * Return: the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - function that modules two integers
 * @a: first integer
 * @b: second integer
 * Return: the remainder of the division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}




